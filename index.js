document.body.addEventListener("keyup", function (e) {
  if (e.key === "ArrowUp") {
    console.log("ArrowUp");
  } else if (e.key === "ArrowDown") {
    console.log("ArrowDown");
  } else if (e.key === "ArrowLeft") {
    console.log("ArrowLeft");
  } else if (e.key === "ArrowRight") {
    console.log("ArrowRight");
  }
});
